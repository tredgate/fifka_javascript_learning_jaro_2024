function add(a, b) {
  const result = a + b;
  console.log("Výsledek sčítání je: " + result);
}

function subtract(a, b) {
  const result = a - b;
  console.log("Výsledek odčítání je: " + result);
}

function multiply(a, b) {
  const result = a * b;
  console.log("Výsledek násobení je: " + result);
}

function divide(a, b) {
  const result = a / b;
  console.log("Výsledek dělení je: " + result);
}
